// Example 1: Select an element using getElementById()

let heading = document.getElementById("heading");
console.log(heading);


// Example 2: Select the first matching element using querySelector()

let paragraph = document.querySelector(".text");
console.log(paragraph);


// Example 3: Select all matching elements using querySelectorAll()

let paragraphs = document.querySelectorAll(".text");
console.log(paragraphs);


// Example 4: Select elements using getElementsByClassName()

let classElements = document.getElementsByClassName("text");
console.log(classElements);


// Example 5: Select elements using getElementsByTagName()

let headings = document.getElementsByTagName("h2");
console.log(headings);